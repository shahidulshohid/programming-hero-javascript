// Take four parameters. Multiply the four numbers and then return the result 
function fourParamMultiply(num1, num2, num3, num4){
    const result = num1 * num2 * num3 * num4;
    return result;
    
}
const totalResult = fourParamMultiply(10, 20, 30, 40);
